#include "main.h"

/**
 * main - Entry point of the program
 *
 * Description: Prints each character of "_putchar" followed by a newline
 * Return: Always 0 (Success)
 */
int main(void)
{
	_putchar('_');      /* Print underscore character */
	_putchar('p');      /* Print 'p' character */
	_putchar('u');      /* Print 'u' character */
	_putchar('t');      /* Print 't' character */
	_putchar('c');      /* Print 'c' character */
	_putchar('h');      /* Print 'h' character */
	_putchar('a');      /* Print 'a' character */
	_putchar('r');      /* Print 'r' character */
	_putchar('\n');     /* Print newline character */

	return (0);
}

